mark = int(input("Please input your mark here: "))

if mark >= 80:
    print("Your grade is A+")
elif mark >= 70:
    print("Your grade is A")
elif mark >= 60:
    print("Your grade is A-")
elif mark >= 50:
    print("Your grade is B")
elif mark >= 40:
    print("Your grade is C")
elif mark >= 33:
    print("Your grade is D")
else :
    print ("You failed")